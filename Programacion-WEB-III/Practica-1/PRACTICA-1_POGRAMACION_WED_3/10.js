//¿Cuando es conveniente utilizar un callback, y cuando es necesario utilizar una
//promesa?
/*
Es conveniente utilizar un callback cuando se necesita ejecutar una función después de completar una 
tarea asíncrona simple, especialmente en código legacy o cuando no se requiere encadenamiento de operaciones.
Es necesario utilizar una promesa cuando se requiere manejar múltiples operaciones asíncronas, 
encadenar tareas, o gestionar errores de forma más estructurada y legible. Las promesas permiten 
un flujo de control más claro y facilitan el uso de async/await.
*/
